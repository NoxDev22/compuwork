    public void setEmployeeToCard(Employee emp){
        this.cardNameEmp.setText(emp.getName());
        this.cardLastNameEmp.setText(emp.getLastName());
        this.cardPostEmp.setText("Cargo: "+emp.getPost());
        this.cardDocumentEmp.setText("C.C: "+emp.getDocument());
        this.cardTelEmp.setText("Cel: "+emp.getPhone());
        this.cardSalaryEmp.setText("Salario: "+emp.getSalary());
        this.cardDateEmp.setText("Ingreso: "+emp.getEntryDate());
        this.cardTemporaryEmp.setText("Temporal: "+emp.getTemporary());
        
    }
    
