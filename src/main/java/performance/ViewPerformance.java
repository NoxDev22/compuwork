    private void txtSearchEmpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtSearchEmpMouseClicked
        String document = this.controller.getDocument();
        Employee emp = this.controller.viewPerformance();
        
        if(document != ""){
            this.readEmployee.setVPerformance(this);
            this.readEmployee.setNameEmployee(emp.getName()+" "+emp.getLastName());
            this.readEmployee.setPerformance(emp.getPerformance());
            this.readEmployee.setVisible(true);
            this.setVisible(false);
            
        }else {
            this.showMessage("¡Error no se ha realizado ninguna busqueda!");
        }
    }//GEN-LAST:event_txtSearchEmpMouseClicked

