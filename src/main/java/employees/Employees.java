    private void cardTxtPerformanceMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cardTxtPerformanceMouseClicked
        String document = this.controller.getDocument();
        Employee emp = this.controller.viewPerformance();
        
        if(document != ""){
            this.performance.setEmployee(this);
            this.performance.setNameEmployee(emp.getName()+" "+emp.getLastName());
            this.performance.setPerformance(emp.getPerformance());
            this.performance.setVisible(true);
            this.setVisible(false);
            
        }else {
            this.showMessage("¬°Error no se ha realizado ninguna busqueda!");
        }
    }//GEN-LAST:event_cardTxtPerformanceMouseClicked

