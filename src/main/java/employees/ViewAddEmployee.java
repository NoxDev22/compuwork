    private void txtAddEmployeeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtAddEmployeeMouseClicked
        String name = this.fieldName.getText();
        String lastName = this.fieldLastName.getText();
        String document = this.fieldDocument.getText();
        String post = this.fieldPost.getText();
        String phone = this.fieldPhone.getText();
        String date = this.fieldEntryDate.getText();
        String salary = this.fieldSalary.getText();
        String temporary = this.boxTermporary.getSelectedItem().toString();
        
        this.employee.createEmployee(document, name, lastName, post, phone, date, salary, temporary);
    }//GEN-LAST:event_txtAddEmployeeMouseClicked

