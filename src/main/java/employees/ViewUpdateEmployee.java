    private void txtUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtUpdateMouseClicked
        String name = this.fieldName.getText();
        String lastName = this.fieldLastName.getText();
        String document = this.fieldDocument.getText();
        String post = this.fieldPost.getText();
        String phone = this.fieldPhone.getText();
        String date = this.fieldEntryDate.getText();
        String salary = this.fieldSalary.getText();
        String temporary = this.boxTemporary.getSelectedItem().toString();
        
        this.employee.updateEmployee(document, name, lastName, post, phone, date, salary, temporary);
    }//GEN-LAST:event_txtUpdateMouseClicked

